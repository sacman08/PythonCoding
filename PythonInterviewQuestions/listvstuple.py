# lists vs tuples
# lists are mutable, tuples are immutable
# Both are iterable
# both can count and index themselves
# both can be used with "in" search
# both can contain any values

# Lists are for seq of items of the same types
# Tuples are for seq of items of different types

# Lists are for:
# files
# int
# filenames
# strings

# Tuples are for:
# structs
# records
# static data